public abstract class Calculator {

    public abstract double getOperand(String operand);

    public abstract String getOperation(String operation);

    public abstract void calculation(double operand1, double operand, String operation);

    public abstract double adding(double operand1, double operand2);

    public abstract double subtraction(double operand1, double operand2);

    public abstract double multiplication(double operand1, double operand2);

    public abstract double division(double operand1, double operand2);


}
